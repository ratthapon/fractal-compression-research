function figDIfferentSpecs( )
%FIGDIFFERENTSPECS Visualize the freq spectrum, mel spectrum, surf freq spectrum

%% read signals data
% original signals and their interpolated signal
wavDir = 'F:/IFEFSR/ExpSphinx/';
wavName = '/wav/an4_clstk/fash/an251-fash-b.raw';
sigO8 = rawread([wavDir 'BASE8' wavName]);
sigO16 = rawread([wavDir 'BASE16' wavName]);
sigInt816 = interp(sigO8, 2, 4, 1.0);

% reconstructed using fractal coding
sigR8_rbs2 = rawread([wavDir 'FCMATLABRBS2FS816' wavName]);
sigR16_rbs2 = rawread([wavDir 'FCMATLABRBS2FS1616' wavName]);
sigR8_rbs4 = rawread([wavDir 'FCMATLABRBS4FS816' wavName]);
sigR16_rbs4 = rawread([wavDir 'FCMATLABRBS4FS1616' wavName]);
% sigR8_rbs8 = rawread([wavDir 'FCMATLABRBS8FS816' wavName]);
% sigR16_rbs8 = rawread([wavDir 'FCMATLABRBS8FS1616' wavName]);

% reconstructed using fractal coding then apply low-pass filter
sigR8_rbs4_lp5500hz = rawread([wavDir 'FCMATLABRBS4LP6875N16FS816' wavName]);
sigR16_rbs4_lp5500hz = rawread([wavDir 'FCMATLABRBS4LP6875N16FS1616' wavName]);
% sigR816_adp4_128_lp6000hz = rawread([wavDir 'ADPFCMATLABRBS4T128LP9375N16FS816' wavName]);
% sigR16_adp4_128_lp6000hz = rawread([wavDir 'ADPFCMATLABRBS4T128LP9375N16FS1616' wavName]);

%% extract the informations
[ Tw, Ts, preemAlpha, M, C, L, LF, HF ] = getMFCCSphinxParams();
% original signals
[MFCCO8, FBEO8, SPECO8] = mfcc( sigO8, 8000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );
SPECO8 = [SPECO8; zeros(size(SPECO8))]; % pad zeros
[MFCCO16, FBEO16, SPECO16] = mfcc( sigO16, 16000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );
% interpolated signal
[MFCCI816, FBEI816, SPECI816] = mfcc( sigInt816, 16000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );

% reconstruct signals
[MFCCR816_rbs2, FBER816_rbs2, SPECR816_rbs2] = mfcc( sigR8_rbs2, 16000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );
[MFCCR16_rbs2, FBER16_rbs2, SPECR16_rbs2] = mfcc( sigR16_rbs2, 16000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );

[MFCCR816_rbs4, FBER816_rbs4, SPECR816_rbs4] = mfcc( sigR8_rbs4, 16000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );
[MFCCR16_rbs4, FBER16_rbs4, SPECR16_rbs4] = mfcc( sigR16_rbs4, 16000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );

[MFCCR816_rbs4_lp5500hz, FBER816_rbs4_lp5500hz, SPECR816_rbs4_lp5500hz] = mfcc( sigR8_rbs4_lp5500hz, 16000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );
[MFCCR16_rbs4_lp5500hz, FBER16_rbs4_lp5500hz, SPECR16_rbs4_lp5500hz] = mfcc( sigR16_rbs4_lp5500hz, 16000, ...
    Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );

% [MFCCR816_adp4_128_lp5500hz, FBER816_adp4_128_lp5500hz, SPECR816_adp4_128_lp5500hz] = mfcc( sigR816_adp4_128_lp6000hz, 16000, ...
%     Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );
% [MFCCR16_adp4_128_lp5500hz, FBER16_adp4_128_lp5500hz, SPECR16_adp4_128_lp5500hz] = mfcc( sigR16_adp4_128_lp6000hz, 16000, ...
%     Tw, Ts, preemAlpha, @hamming, [LF HF], M, C+1, L );

%% show the different information of two different features
fig = figure(1);
plotCMPSpec(fig,...
    SPECO16, 'Spectrum of original signal 16 kHz', ...
    SPECO8, 'Spectrum of original signal 8 kHz' ...
    )

fig = figure(2);
surfSpec(fig,...
    SPECO16, 'Spectrum of original signal 16 kHz', ...
    SPECO8, 'Spectrum of original signal 8 kHz' ...
    )

fig = figure(3);
surfSpec(fig,...
    SPECO16, 'Spectrum of original signal 16 kHz', ...
    SPECI816, 'Spectrum of interpolated signal 8->16 kHz' ...
    )

fig = figure(4);
surfSpec(fig,...
    SPECR16_rbs4, 'Spectrum of reconstructed signal 16->16 kHz', ...
    SPECR816_rbs4, 'Spectrum of reconstructed signal 8->16 kHz' ...
    )

fig = figure(5);
surfSpec(fig,...
    SPECR816_rbs2, 'Spectrum of reconstructed signal rbs 2 8->16 kHz', ...
    SPECR816_rbs4, 'Spectrum of reconstructed signal rbs 4 8->16 kHz' ...
    )

fig = figure(6);
surfSpec(fig,...
    SPECR16_rbs4_lp5500hz, 'Spectrum of low-passed reconstructed signal rbs 4 16->16 kHz', ...
    SPECR816_rbs4_lp5500hz, 'Spectrum of low-passed reconstructed signal rbs 4 8->16 kHz' ...
    )


% fig = figure(7);
% surfSpec(fig,...
%     SPECR16_adp4_128_lp5500hz, 'Low-passed reconstructed signal ADP 16->16 kHz', ...
%     SPECR816_adp4_128_lp5500hz, 'Low-passed reconstructed signal ADP 8->16 kHz' ...
%     )

fig = figure(8);
plotCMPMelSpec(fig,...
    FBEO16, 'Mel spectrum of original signal 16 kHz', ...
    FBEO8, 'Mel spectrum of original signal 8 kHz' ...
    )

fig = figure(9);
surfSpec(fig,...
    SPECO8, 'Spectrum of original signal 8 kHz', ...
    SPECR816_rbs4_lp5500hz, 'Spectrum of low-passed reconstructed signal rbs 4 8->16 kHz' ...
    )

end

