function y = avgBlk2(x)
[L1 L2]= size(x);
% row --------------------------
x1 = x(:,1:2:L2);
x2 = x(:,2:2:L2);
% ------------------------------

y1 = (double(x1)+double(x2))./2; 

% column -----------------------
x3 = y1(1:2:L1,:);
x4 = y1(2:2:L1,:);
% ------------------------------

y2 = (double(x3)+double(x4))./2;
y = y2;

